package mdls.ventas_desa;

public class CENSO_ITEM {
	
	public CENSO_ITEM()
	{
		
	}
	
	int iditem;
	String item;
	String tipodato;
	String excluyente;
	String nulo;
	
	public int getIditem() 
	{
		return iditem;
	}
	
	public void setIditem(int iditem) 
	{
		this.iditem = iditem;
	}
	
	public String getItem() 
	{
		return item;
	}
	
	public void setItem(String item) 
	{
		this.item = item;
	}
	
	public String getTipodato() 
	{
		return tipodato;
	}
	
	public void setTipodato(String tipodato) 
	{
		this.tipodato = tipodato;
	}
	
	public String getExcluyente() 
	{
		return excluyente;
	}
	
	public void setExcluyente(String excluyente) 
	{
		this.excluyente = excluyente;
	}
	
	public String getNulo() 
	{
		return nulo;
	}
	
	public void setNulo(String nulo) 
	{
		this.nulo = nulo;
	}
	
}