package mdls.ventas_desa;

public class Marca {
	
	public Marca()
	{
		
	}
	
	int idmarca;
	String nombre;
	
	public int getIdmarca() {
		return idmarca;
	}
	public void setIdmarca(int idmarca) {
		this.idmarca = idmarca;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	

}
