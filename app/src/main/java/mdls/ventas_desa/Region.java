package mdls.ventas_desa;

public class Region {
	
	public Region()
	{
		
	}
	
	int idregion;
	String nombre;
	
	public int getIdregion() {
		return idregion;
	}
	public void setIdregion(int idregion) {
		this.idregion = idregion;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	

}
